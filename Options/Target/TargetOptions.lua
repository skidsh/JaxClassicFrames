function GetJCFTargetOptions()
    return {
        order = 2,
        name = "Target",
        handler = JCFTargetSettings,
        type = "group",
        args = {
            targetEnable = {
                order = 1,
                width = "full",
                name = "Enable",
                desc = "Enable or disable",
                type = "toggle",
                get = "GetEnabled",
                set = "SetEnabled",
            },
            targetClassPortraitEnable = {
                disabled = function() return not JCFTargetSettings:GetEnabled() end,
                order = 2,
                width = "full",
                name = "Class Icon Portrait",
                desc = "Enable or disable class icon portrait",
                type = "toggle",
                get = "GetClassPortraitEnabled",
                set = "SetClassPortraitEnabled",
            },
            targetClassColorHealthEnable = {
                disabled = function() return not JCFTargetSettings:GetEnabled() end,
                order = 3,
                width = "full",
                name = "Class Color Health Bar",
                desc = "Enable or disable class color target health bar",
                type = "toggle",
                get = "GetClassColorHealthEnabled",
                set = "SetClassColorHealthEnabled",
            },
            targetHideNameBackground = {
                disabled = function() return not JCFTargetSettings:GetEnabled() end,
                order = 4,
                width = "full",
                name = "Hide Name Background",
                desc = "Enable or disable hiding the name background",
                type = "toggle",
                get = "GetHideNameBackground",
                set = "SetHideNameBackground",
            },
            targetCastBar = {
                name = "Target Castbar",
                handler = JCFTargetSettings,
                type = "group",
                args = {
                    targetCastBarScale = {
                        disabled = function() return not JCFTargetSettings:GetEnabled() end,
                        order = 1,
                        name = "Target Castbar Scale",
                        desc = "Set castbar scale",
                        type = "range",
                        get = "GetCastBarScale",
                        set = "SetCastBarScale",
                        softMax = 3,
                        min = 0
                    },
                    targetCastBarReanchor = {
                        order = 2,
                        width = "full",
                        name = "Reanchor Castbar",
                        disabled = function() return not JCFTargetSettings:GetEnabled() end,
                        desc = "Enable or disable reanchoring of the target castbar",
                        type = "toggle",
                        get = "GetCastBarReanchor",
                        set = "SetCastBarReanchor",
                    },
                    targetCastBarXOffset = {
                        order = 3,
                        name = "Target Castbar X offset",
                        desc = "Set castbar X offset",
                        disabled = function() return not JCFTargetSettings:GetEnabled() or not JCFTargetSettings:GetCastBarReanchor() end,
                        type = "range",
                        get = "GetCastBarXOffset",
                        set = "SetCastBarXOffset",
                        min = -1000,
                        max = 1000,
                        softMin = -500,
                        softMax = 500,
                        step = 1,
                    },
                    targetCastBarYOffset = {
                        order = 4,
                        name = "Target Castbar Y offset",
                        desc = "Set castbar Y offset",
                        disabled = function() return not JCFTargetSettings:GetEnabled() or not JCFTargetSettings:GetCastBarReanchor() end,
                        type = "range",
                        get = "GetCastBarYOffset",
                        set = "SetCastBarYOffset",
                        min = -1000,
                        max = 1000,
                        softMin = -500,
                        softMax = 500,
                        step = 1,
                    },
                }
            },
            targetOfTarget = {
                name = "Target of Target",
                handler = JCFTargetSettings,
                type = "group",
                args = {
                    targetTotReanchor = {
                        order = 2,
                        width = "full",
                        name = "Reanchor Target of Target",
                        disabled = function() return not JCFTargetSettings:GetEnabled() end,
                        desc = "Enable or disable reanchoring of the target of target frame",
                        type = "toggle",
                        get = "GetTotReanchor",
                        set = "SetTotReanchor",
                    },
                    targetTotXOffset = {
                        order = 3,
                        name = "Target of Target X offset",
                        desc = "Set target of target X offset",
                        disabled = function() return not JCFTargetSettings:GetEnabled() or not JCFTargetSettings:GetTotReanchor() end,
