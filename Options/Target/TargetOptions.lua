                        get = "GetTotXOffset",
                        set = "SetTotXOffset",
                        min = -1000,
                        max = 1000,
                        softMin = -500,
                        softMax = 500,
                        step = 1,
                    },
                    targetTotYOffset = {
                        order = 4,
                        name = "Target of Target Y offset",
                        desc = "Set target of target Y offset",
                        disabled = function() return not JCFTargetSettings:GetEnabled() or not JCFTargetSettings:GetTotReanchor() end,
                        type = "range",
                        get = "GetTotYOffset",
                        set = "SetTotYOffset",
                        min = -1000,
                        max = 1000,
                        softMin = -500,
                        softMax = 500,
                        step = 1,
                    },
                }
            }
        },
    }
end